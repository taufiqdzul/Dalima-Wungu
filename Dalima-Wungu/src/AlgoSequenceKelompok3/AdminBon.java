    private void showTabel(String id_transaksi){
        System.out.println(id_transaksi);
        int row = jTableStruk.getRowCount();
        for(int i=0; i<row; i++){
            tabelmodel.removeRow(0);
        }
        try{
            con = Koneksi.getConnection();
            stm = con.createStatement();
            String SQL = ("select penyewa.nama, penyewa.alamat, barang.kode_barang, barang.nama_barang, barang.jenis_barang, barang.harga_sewa, sewa.qty, sewa.total, transaksi.tanggal_pinjam, transaksi.tanggal_pengembalian, transaksi.diskon, transaksi.total_transaksi, transaksi.pembayaran from penyewa, barang, sewa, transaksi where penyewa.id_penyewa = sewa.id_penyewa and barang.kode_barang = sewa.kode_barang and transaksi.id_transaksi = sewa.id_transaksi and transaksi.id_transaksi = '"+id_transaksi+"'");
            rs = stm.executeQuery(SQL);
            int total_transaksi = 0;
            int pembayaran = 0;
            while(rs.next()){
                String data[] = {rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8)};
                tabelmodel.addRow(data);
                jLabelNama.setText(rs.getString("nama"));
                jLabelAlamat.setText(rs.getString("alamat"));
                jLabelTglPinjam.setText(rs.getString("tanggal_pinjam"));
                jLabelTglKembali.setText(rs.getString("tanggal_pengembalian"));
                jLabelDiskon.setText(rs.getString("diskon"));
                jLabelTotal.setText(rs.getString("total_transaksi"));
                total_transaksi = rs.getInt("total_transaksi");
                pembayaran = rs.getInt("pembayaran");
            }
            int kembalian = pembayaran-total_transaksi;
            jLabelKembalian.setText(Integer.toString(kembalian));
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

